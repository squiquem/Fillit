#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		neg;
	int		nb;

	i = 0;
	neg = 1;
	nb = 0;
	while (ft_isspace(s[i]))
		i++;
	if (s[i] == '-' || s[i] == '+' || (s[i] >= '0' && s[i] <= '9'))
	{
		if (s[i] == '-')
		{
			neg = -neg;
			i++;
		}
		else if (s[i] == '+')
			i++;
		while (s[i] >= '0' && s[i] <= '9' && s[i])
		{
			nb = nb * 10 + (s[i] - '0');
			i++;
		}
	}
	return (neg * nb);
}
