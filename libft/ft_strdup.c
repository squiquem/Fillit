#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;

	if (!(dest = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))) || !s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
